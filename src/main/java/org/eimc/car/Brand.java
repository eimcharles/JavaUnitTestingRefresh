package org.eimc.car;

/**
 *      The Brand enum represents a list
 *      of supported car brands.
 *
 *      It defines a fixed set of constant values
 *      that can be used to specify
 *      the brand of a car within the application.
 */

public enum Brand {

    VOLKSWAGEN,
    BMW,
    MAZDA,
    HONDA,
    MERCEDES

}
